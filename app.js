function localeForLanguage(language) {
  if (language === 'de') {
    return 'de-DE';
  }
  if (language === 'nl') {
    return 'nl-NL';
  }
  if (language === 'fr') {
    return 'fr-FR';
  }
  return 'en-GB';
}

